package com.canchitodev.example.response;

public class BooksResponse {
	
	private Long bookId;
	private String title;
	private Integer year;
	private String price;
	private Long authorId;
	
	public BooksResponse() {}

	public BooksResponse(Long bookId, String title, Integer year, String price, Long authorId) {
		this.bookId = bookId;
		this.title = title;
		this.year = year;
		this.price = price;
		this.authorId = authorId;
	}

	public Long getBookId() {
		return bookId;
	}

	public void setBookId(Long bookId) {
		this.bookId = bookId;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public Integer getYear() {
		return year;
	}

	public void setYear(Integer year) {
		this.year = year;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	public Long getAuthorId() {
		return authorId;
	}

	public void setAuthorId(Long authorId) {
		this.authorId = authorId;
	}

	@Override
	public String toString() {
		return "BooksResponse [bookId=" + bookId + ", title=" + title + ", year=" + year + ", price=" + price
				+ ", authorId=" + authorId + "]";
	}
}